package fetch
import (
	"encoding/json"
	"io"
	"fmt"
	"net/http"
)

func GetData[
	T MapResult | 
	ExploreResult | 
	CatchResult,
	] (url string, result T) (T, []byte, error){
	body, err := fetchUrl(url)
	if err != nil {
		return result, nil, err
	}
	result, err = ParseResult(result, body)
	if err != nil {
		return result, nil, err
	}
	return result, body, nil
}

func fetchUrl(url string) ([]byte, error){
	response, err := http.Get(url)
	if err != nil {
		return nil, err
	}

	defer response.Body.Close()
	body, err := io.ReadAll(response.Body)
	if err != nil {
		return nil, err
	}
	return body, nil
}

func ParseResult[T MapResult | ExploreResult | CatchResult](result T, body []byte) (T , error){
	if string(body) == "Not Found" {
		return result, fmt.Errorf("Not Found")
	}
	err := json.Unmarshal(body, &result)
	if err != nil {
		return result, fmt.Errorf("Error parsing json: %v", err)
	}
	return result, nil
}

type MapResult struct {
	Count    int    `json:"count"`
	Next     string `json:"next"`
	Previous string    `json:"previous"`
	Results  []struct {
		Name string `json:"name"`
		URL  string `json:"url"`
	} `json:"results"`
}

